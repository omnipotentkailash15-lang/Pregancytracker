def _get_week_message(week: int) -> str:
    if week <= 4:
        return "नई शुरुआत का पवित्र समय। शांत रहें, प्रसन्न रहें।"
    elif week <= 8:
        return "शिशु के सभी प्रमुख अंग बन रहे हैं। पोषण पर ध्यान दें।"
    elif week <= 13:
        return "पहली तिमाही अपनी पूर्णता पर। आप अद्भुत कर रही हैं।"
    elif week <= 20:
        return "दूसरी तिमाही — ऊर्जा और आनंद का समय। गर्भसंस्कार शुरू करें।"
    elif week <= 27:
        return "शिशु बड़ा हो रहा है। उससे बात करें, प्यार भेजें।"
    elif week <= 35:
        return "तीसरी तिमाही — प्रसव की तैयारी शुरू करें।"
    else:
        return "बस थोड़ा और! शिशु जल्द ही आपसे मिलेगा।"


