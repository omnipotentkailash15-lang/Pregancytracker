def get_weekly_data(week: int) -> dict:
    """Get weekly data, interpolating from nearest template."""
    if week in WEEKLY_TEMPLATES:
        return WEEKLY_TEMPLATES[week]

    # Find nearest template
    keys = sorted(WEEKLY_TEMPLATES.keys())
    nearest = min(keys, key=lambda x: abs(x - week))
    base = WEEKLY_TEMPLATES[nearest].copy()

    # Customize message based on actual week
    base["aaj_ka_sandesh"] = _get_week_message(week)
    base["baby_size"] = _get_baby_size(week)
    return base


